package com.hsapi.cloud.part.invoicing;

public class RPAcount {
	private Integer orgid;
	private Integer guestId;
	private String guestName;
	private Float cgr;
	private Float xsr;
	private Float xsc;
	private Float cgc;
	private Float hz;
	
	public Integer getOrgid() {
		return orgid;
	}
	public void setOrgid(Integer orgid) {
		this.orgid = orgid;
	}
	public Integer getGuestId() {
		return guestId;
	}
	public void setGuestId(Integer guestId) {
		this.guestId = guestId;
	}
	public String getGuestName() {
		return guestName;
	}
	public void setGuestName(String guestName) {
		this.guestName = guestName;
	}
	public Float getCgr() {
		return cgr;
	}
	public void setCgr(Float cgr) {
		this.cgr = cgr;
	}
	public Float getXsr() {
		return xsr;
	}
	public void setXsr(Float xsr) {
		this.xsr = xsr;
	}
	public Float getXsc() {
		return xsc;
	}
	public void setXsc(Float xsc) {
		this.xsc = xsc;
	}
	public Float getCgc() {
		return cgc;
	}
	public void setCgc(Float cgc) {
		this.cgc = cgc;
	}
	public Float getHz() {
		return hz;
	}
	public void setHz(Float hz) {
		this.hz = hz;
	}
}
